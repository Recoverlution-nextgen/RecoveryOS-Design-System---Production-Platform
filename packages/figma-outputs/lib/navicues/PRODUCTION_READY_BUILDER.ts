function createNaviCue(
  id: number,
  family: SparkFamily,
  modality: Modality,
  responseType: ResponseType,
  textLine: string,
  pillarId: PillarId,
  schema: string,
  kbeTarget: KBELayer,
  track: 'clinical' | 'guru' | 'infinite',
  difficulty: number,
  duration: number,
  PILLARS_DATA: any
): NaviCue {
  const pillar = PILLARS_DATA[pillarId];
  const schemaName = schema.split('-').map((w: string) => w.charAt(0).toUpperCase() + w.slice(1)).join(' ');
  
  const coreBeliefs: Record<string, string> = {
    'shame': 'I am fundamentally flawed or unlovable',
    'control': 'I must control everything or chaos will happen',
    'abandonment': 'People will leave me if I let them in',
    'perfectionism': 'I must be perfect to be acceptable',
    'victimhood': 'Things happen to me, I have no power',
    'emotional-suppression': 'Feelings are dangerous and should be hidden',
    'people-pleasing': 'I must make others happy to be safe',
    'scarcity': 'There is never enough for me',
    'comparison': 'My worth depends on being better than others',
    'catastrophizing': 'The worst will happen',
    'identity-fusion': 'I am what I do / what happened to me',
    'safety-seeking': 'I can never truly be safe',
  };

  return {
    id: `nc.${String(id).padStart(3, '0')}`,
    name: `${family} ${id}`,
    family,
    modality,
    response_type: responseType,
    text_line: textLine,
    pillar_id: pillarId,
    pillar_name: pillar.name,
    pillar_color: pillar.color,
    schema: schema,
    schema_name: schemaName,
    core_belief: coreBeliefs[schema] || '',
    kbe_target: kbeTarget,
    track,
    difficulty_level: difficulty,
    duration_minutes: duration,
    tags: [family, schema, pillarId],
  };
}

